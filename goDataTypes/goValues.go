package main

import "fmt"

func main() {
	//string
	fmt.Println("go" + "lang")

	//int
	fmt.Println("1+1=",1+1)

	//float
	fmt.Println("7.0/3.0=",7.0/3.0)

	//bool
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	
	var b1 bool = true
	var b2 = true
	var b3 bool
	b4 := true
	fmt.Println(b1)
	fmt.Println(b2)
	fmt.Println(b3)
	fmt.Println(b4)
}